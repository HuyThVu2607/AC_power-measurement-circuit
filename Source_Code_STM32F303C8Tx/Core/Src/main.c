static void Get_Data_And_Handle(void)
{
		Time_counter= Time_counter+1;
		Adc_vol=0;
		Adc_amp=0;
	
		//Get Voltage value
    for(int i=0; i<10;i++)
		{
		HAL_ADC_Start(&hadc1);
		Adc_vol =Adc_vol+HAL_ADC_GetValue(&hadc1);
		HAL_ADC_Stop(&hadc1);
		}
		Adc_vol=Adc_vol/10;
		
		//Get Ampere value
		for(int i=0; i<10;i++){
		HAL_ADC_Start(&hadc2);
		Adc_amp =Adc_amp+HAL_ADC_GetValue(&hadc2);
		HAL_ADC_Stop(&hadc2);
		HAL_GPIO_TogglePin(GPIOC,GPIO_PIN_13);
		}
		Adc_amp=Adc_amp/10;
		
		//Get Phase value
		Phase = 0.8;
		
		//Data Hanlde 
		Vol_result=(Adc_vol*3.3*129.9)/4095;
		Amp_result=(Adc_amp*3.3*1.37)/4095;
		Watg_result = Vol_result*Amp_result*Phase;
	
		Energy = Energy + Watg_result;
		
		Watg_per_Hour = Energy/(1000*3600);
		
}
	

	

static void Send_Data_Esp32(void)
{
    char mess[32];
    for(int i = 0; i < 2; i++) {
        switch (i) {
            case 0:
                //Send Vol_Value
                sprintf(mess, "%0.3f ; %0.3f ; %0.3f \r\n", Vol_result,Amp_result,Watg_result);
                break;
            default:
                break;
        }
        HAL_UART_Transmit(&huart1, (uint8_t *)mess, sizeof(mess), HAL_MAX_DELAY);
       
    }
}


